// Function to create a random variation of an existing hero
export function createRandomHeroVariation(baseHero) {
  const variations = [
    { suffix: "Prime", alignment: "good" },
    { suffix: "Dark", alignment: "bad" },
    { suffix: "Ultimate", alignment: "good" },
    { suffix: "Shadow", alignment: "neutral" },
    { suffix: "Omega", alignment: "good" },
    { suffix: "Alpha", alignment: "good" },
    { suffix: "Neo", alignment: "good" },
    { suffix: "Quantum", alignment: "neutral" }
  ];

  const variation = variations[Math.floor(Math.random() * variations.length)];
  const newHero = JSON.parse(JSON.stringify(baseHero)); // Deep clone

  // Modify the hero
  newHero.id = Date.now() + Math.floor(Math.random() * 1000);
  newHero.name = `${baseHero.name} ${variation.suffix}`;
  newHero.slug = `${newHero.id}-${newHero.name.toLowerCase().replace(/\s+/g, '-')}`;
  newHero.biography.alignment = variation.alignment;

  // Randomly modify power stats
  Object.keys(newHero.powerstats).forEach(stat => {
    const modifier = Math.floor(Math.random() * 21) - 10; // -10 to +10
    newHero.powerstats[stat] = Math.max(1, Math.min(100, (newHero.powerstats[stat] || 50) + modifier));
  });

  return newHero;
}